#!/usr/bin/env node
// Emits a SQL file that inserts the seeded lessons WITHOUT embeddings.
// (OpenAI quota was exhausted at seed time — embeddings will be back-filled
// by /api/agent-memory-backfill-embeddings once the OpenAI key is restored.)
//
// Output: scripts/.seed-agent-memory.sql
// Idempotent: skips rows where (tenant, role, title) already exists.

const fs = require('fs');
const path = require('path');

const seederCode = fs.readFileSync(path.resolve(__dirname, 'seed-agent-memory.js'), 'utf8');
const m = seederCode.match(/const LESSONS = (\[[\s\S]*?\n\];)/);
if (!m) { console.error('Could not extract LESSONS array'); process.exit(1); }
// eslint-disable-next-line no-new-func
const LESSONS = new Function('return ' + m[1].replace(/;$/, ''))();
console.error(`[emit] extracted ${LESSONS.length} lessons`);

function sqlString(s) { return "'" + String(s).replace(/'/g, "''") + "'"; }
function sqlJsonArray(arr) { return "'" + JSON.stringify(arr || []).replace(/'/g, "''") + "'::jsonb"; }

const lines = [];
lines.push('-- AGENT MEMORY SEED — generated by scripts/_seed-emit-sql.js');
lines.push('-- Embeddings NULL — backfill via /api/agent-memory-backfill-embeddings');
lines.push('-- Idempotent: skips rows where (tenant, role, title) already exists.');
lines.push('');

let i = 0;
for (const lesson of LESSONS) {
  i++;
  lines.push(`-- ${i}: ${lesson.role} | ${lesson.title}`);
  lines.push(`INSERT INTO public.agent_role_memory (tenant_id, agent_role, title, content, category, validation_status, tags, usage_count)`);
  lines.push(`SELECT t.id, ${sqlString(lesson.role)}, ${sqlString(lesson.title)}, ${sqlString(lesson.content)},`);
  lines.push(`       ${sqlString(lesson.category)}, 'auto', ${sqlJsonArray(lesson.tags)}, 0`);
  lines.push(`FROM public.tenants t`);
  lines.push(`WHERE t.slug = 'heath'`);
  lines.push(`  AND NOT EXISTS (SELECT 1 FROM public.agent_role_memory m WHERE m.tenant_id = t.id AND m.agent_role = ${sqlString(lesson.role)} AND m.title = ${sqlString(lesson.title)});`);
  lines.push('');
}

const outPath = path.resolve(__dirname, '.seed-agent-memory.sql');
fs.writeFileSync(outPath, lines.join('\n'));
console.error(`[emit] wrote ${outPath} (${LESSONS.length} lessons, no embeddings)`);
